    async def get_openai_response(self, user_text: str, user_name: str) -> str:
        """–ü–æ–ª—É—á–µ–Ω–∏–µ –æ—Ç–≤–µ—Ç–∞ –æ—Ç OpenAI API —Å –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ–º –æ—Ñ–∏—Ü–∏–∞–ª—å–Ω–æ–π –±–∏–±–ª–∏–æ—Ç–µ–∫–∏"""
        try:
            import openai
            import os
            
            api_key = os.getenv('OPENAI_API_KEY')
            if not api_key or not api_key.startswith('sk-'):
                logger.warning(f"OpenAI API –∫–ª—é—á –Ω–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã–π –∏–ª–∏ –æ—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç.")
                return None

            client = openai.AsyncOpenAI(api_key=api_key)
            
            response = await client.chat.completions.create(
                model="gpt-4",
                messages=[
                    {
                        "role": "system", 
                        "content": self.create_hr_prompt(user_text, user_name)
                    },
                    {
                        "role": "user", 
                        "content": user_text
                    }
                ],
                max_tokens=200,
                temperature=0.7,
                timeout=30
            )
            
            ai_text = response.choices[0].message.content.strip()
            logger.info(f"‚úÖ OpenAI –æ—Ç–≤–µ—Ç –¥–ª—è {user_name}: {ai_text[:50]}...")
            return ai_text
            
        except openai.AuthenticationError:
            logger.error("–û—à–∏–±–∫–∞ –∞—É—Ç–µ–Ω—Ç–∏—Ñ–∏–∫–∞—Ü–∏–∏ OpenAI: –Ω–µ–≤–µ—Ä–Ω—ã–π API –∫–ª—é—á.")
            return None
        except openai.RateLimitError:
            logger.error("–û—à–∏–±–∫–∞ OpenAI: –ø—Ä–µ–≤—ã—à–µ–Ω –ª–∏–º–∏—Ç –∑–∞–ø—Ä–æ—Å–æ–≤.")
            return None
        except Exception as e:
            logger.error(f"–ù–µ–æ–∂–∏–¥–∞–Ω–Ω–∞—è –æ—à–∏–±–∫–∞ OpenAI: {e}")
            return None

